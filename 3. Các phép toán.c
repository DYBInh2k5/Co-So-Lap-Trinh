/*C hỗ trợ các phép toán như +, -, *, /, và %.*/

#include <stdio.h>

int main() {
    int x = 15, y = 4;
    
    printf("x + y = %d\n", x + y);
    printf("x - y = %d\n", x - y);
    printf("x * y = %d\n", x * y);
    printf("x / y = %d\n", x / y);
    printf("x %% y = %d\n", x % y);
    
    return 0;
}

/*C cung cấp các kiểu dữ liệu như int (số nguyên), float (số thực), char (ký tự), v.v.*/


#include <stdio.h>

int main() {
    int a = 10;        // Số nguyên
    float b = 5.5;     // Số thực
    char c = 'A';      // Ký tự
    
    printf("a = %d\n", a);
    printf("b = %.2f\n", b);
    printf("c = %c\n", c);
    
    return 0;
}
